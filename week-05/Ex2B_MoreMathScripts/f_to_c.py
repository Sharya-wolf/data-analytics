# How do you convert a temperature from Celsius to Fahrenheit?

   # To convert a Celsius to Fahrenheit the formula is c = 5 / 9 * (F - 32)

# Script
Fahrenheit = 167

# Calculations
Celsius = 5 / 9 * (Fahrenheit - 32)

# Results
print(f"The degrees is {Fahrenheit:.1f} fahrenheit")
print(f"The fahrenheit {Fahrenheit:.1f} degrees in Celsius is {Celsius:.1f} degrees")
# Output
# The degrees is 167.0 fahrenheit
# The farenheit 167.0 degrees in Celsius is 75.0 degrees


