# How do you calculate the area of a circle?
     # a) The diameter of a given circle is the same as the day of your birthday
     # b) Create the script

# Script
diameter = 23
radius = diameter / 2

# Calculations
from math import pi

area = pi * (radius ** 2)

# Results
print(f"The area of a circle with radius {radius} is {area:.2f}")

