# 1. How do you convert a temperature from Fahrenheit to Celsius?

#   To convert a Temperature from Celsius to Fahrenheit the formala is
#   F = (9 / 5) * C + 32. 

# Script
Celsius = 75

# Calculations
Fahrenheit = (9 / 5) * Celsius + 32

# Result
print(f"It is {Celsius:.1f} celsuis degrees outside")
print(f"The degrees for {Celsius:.1f} in fahrenheit is {Fahrenheit}")

# Output
 # It is 75.0 celsuis degrees outside
 # The degrees for 75.0 in fahrenheit is 167.0


